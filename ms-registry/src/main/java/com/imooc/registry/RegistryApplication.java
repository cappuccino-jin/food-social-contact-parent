/**
 * @Author cappuccino
 * @Date 2022-01-12 21:21
 */
// 激活 Eureka Server 注册中心组件
@EnableEurekaServer
@SpringBootApplication
public class RegistryApplication {

    public static void main(String[] args) {
        SpringApplication.run(RegistryApplication.class, args);
    }
}
